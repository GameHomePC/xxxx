<h2>Откуда произошло Масонство?</h2>
<p>Есть множество ответов на этот вопрос. Каждый автор обладает своей точкой зрения и зачастую их точки зрения не
    совпадают. Даже сам вопрос может иметь разный смысл в зависимости от того, что мы понимаем под масонством: масонство
    в его современной форме, Гильдии Каменщиков, либо что-то еще. “Королевская Масонская Энциклопедия” перечисляет
    следующие разнообразные теории происхождения франкмасонства: “Масонство ведет свою историю: Даже сам вопрос может
    иметь разный смысл в зависимости от того, что мы понимаем под масонством: масонство в его современной форме, Гильдии
    Каменщиков, либо что-то еще. «Королевская Масонская Энциклопедия» перечисляет следующие разнообразные теории
    происхождения франкмасонства: «Масонство ведет свою историю:</p>

<ol>
    <li>от Библейских Патриархов;</li>
    <li>от языческих мистерий;</li>
    <li>от времен строительства Храма Царя Соломона;</li>
    <li>от Крестовых походов;</li>
    <li>от Ордена Тамплиеров;</li>
    <li>от древнеримской Коллегии Мастеровых;</li>
    <li>от оперативных масонов средневековья;</li>
    <li>от розенкрейцеров XIV века;</li>
    <li>от Оливера Кромвеля;</li>
    <li>от принца Карла Стюарта (по политическим соображениям);</li>
    <li>от сэра Кристофера Рена и строительства собора Св. Павла;</li>
    <li>от доктора Дезагюлье и его соратников в 1717 году».</li>
</ol>

<p>Наиболее распространенной версией происхождения Братства является версияего происхождения в современном виде в 1717
    году. Точно установлено, что Орден существовал как цеховая организация каменщиков с XIV века. Но до основания первой
    Великой Ложи в 1717 году, Братство представляло ряд разрозненных цехов. Эти цеха выполняли зодческие работы.</p>

