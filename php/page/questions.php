<h2>Масоны антиправославные?</h2>

<p>Нет. Мы признаем, что православие является одним из высших духовных достижений человечества. Многие современные
    масоны относят себя к православным. Важно, однако, иметь в виду, что масонство не навязывает братьям религиозных
    догматов. Орден не выступает против каких-либо верований или убеждений. Ни одна религия не выделена, как «лучшая» в
    ряду остальных. Это касается и католичества и иудаизма и православия и ислама. Масонство никогда не
    противопоставляло себя какой-либо религии и всегда приветствовало веру в Бога, как бы Его ни называли отдельные
    люди, и как бы Его Имя ни было записано в различных Священных Книгах.</p>
