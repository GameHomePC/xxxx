<h2>Что такое регулярное масонство?</h2>
<p>Понятие «регулярность» связано с представлениями старейших масонских Великих Лож о правильности осуществления
    масонской практики и о соответствии этой практики основным масонским стандартам. Значение этого слова и этого
    понятия для масонства следует искать, прежде всего, в его прямом смысле. «Регулярность» – Regularity – означает
    «правильность», «обыкновенность». Подавляющее большинство Великих Лож мира объединивших себя в единую цепь,
    консолидировалось вокруг простых принципов, которые названы принципы регулярности.</p>
<p>Регулярность Великой Ложи – основа для ее признания другими Великими Ложами, основа для ее вовлеченности в братскую
    цепь.</p>
<p>Как отметил на конференции Великих мастеров мира в 2002 году Исполнительный секретарь конференции Томас У.Джексон:
    «Сегодня мы подтверждаем, что регулярность Великой Ложи может быть только, если она создана другой регулярной
    Великой Ложей или тремя или более регулярными ложами, находящимися в юрисдикции регулярных Великих Лож. Регулярность
    также зависит от поддержания Великой Ложей в своей практической работе стандартов регулярности. Они включают в себя
    веру в Высшее Существо, наличие Книги Священного Закона на алтаре, доступ к членству только мужчин, исключение
    политических и религиозных дискуссий в Ложе, ограничения в связи с невозможностью братских отношений с нерегулярным
    масонством, и уважение территориального суверенитета других Великих Лож. Последнее требование было недавно изменено
    в связи с возможностью признавать более чем одну Великую Ложу на одной территории, если эти Великие Ложи признают
    друг друга».</p>
<p>Наиболее важный документ для определения принципов регулярности – Базовые принципы признания Объединенной Великой
    Ложи Англии (Basic&nbsp;Principles&nbsp;for&nbsp;recognition), принятые ОВЛА 4 июля 1929 года и Двенадцать пунктов
    регулярности, принятые Великой Национальной Ложей Франции.</p>

<br>
<h2>Великая Ложа России – регулярная Великая Ложа.</h2>
<p>Наиболее известная нерегулярная Великая Ложа в мире – Великий Восток Франции, исключивший в 1877 году из своей
    Конституции упоминание о Великом Строителе Вселенной как Высшем Существе.</p>
<p>Ряд Великих Лож декларируют, что они являются регулярными, но непризнанными Великими Ложами. Среди таких Великих Лож
    Великая Ложа Франции и Объединенная Великая Ложа России. С точки зрения сообщества регулярных Великих Лож, эти
    Великие Ложи не считаются регулярными в связи с нарушением принципа законности присхождения, либо принципа
    исключительности территориальной юрисдикции. То есть такие Великие Ложи либо не были основаны тремя регулярными
    ложами или Великой Ложей, и/или были основаны в ситуации, когда соответствующая территория уже была занята другой
    регулярной Великой Ложей. Регулярным масонством такие структуры не признаны, их члены не считаются масонами.</p>
<p>Регулярное масонство тесно интегрировано внутри себя и объединяет около 80 процентов людей, называющих себя
    масонами.</p>

