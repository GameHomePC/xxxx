<h2>Что такое масонские ландмарки?</h2>
<p>Ландмарки (в масонстве) – ставшие широко известными правила организации масонства, сформулированные относительно
    кратко масонскими писателями на основании исторических исследований; значительная часть масонских исследователей
    полагает эти правила древними и неизменными.</p>
<p>Буквально слово «ландмарки» означает вехи, межи, обозначающие границы земельных участков. В Книге притчей Соломона
    22:28 сказано: «Не передвигай межи давней, которую провели отцы твои».&nbsp;Persy&nbsp;Jantz&nbsp;в статье&nbsp;The&nbsp;landmarks&nbsp;of&nbsp;freemasonry&nbsp;связывает
    происхождение масонского термина «ландмарки» с приведенной библейской цитатой.</p>
<p>Понятие ландмарок впервые возникает в масонском праве&nbsp;в Общем Регламенте Джорджа Пейна, первом руководящем
    документе Великой Ложи Англии «Премьер», составленном в 1720 году и принятом в 1721 году. Статья 39 этого Общего
    Регламента указывает, что «каждая ежегодная Ассамблея Великой Ложи обладает сама по себе властью и правом составлять
    новый Регламент, или изменять настоящий Регламент во неоспоримое благо&nbsp;древнего братства при условии, чтобы во
    всякое время неукоснительно сохранялись древние ландмарки».&nbsp;Перечень ландмарок не был определен.</p>
<p>Наиболее авторитетный и известный перечень ландмарок составил Альберт Макей, поместивший его в 1858 году в
    октябрьское издание American Quarterly Review of Freemasonry
    (http://freemasonry.bcy.ca/grandlodge/landmarks.html).<em>&nbsp;</em>Список Макея состоит из 25 ландмарок.
    Существует множество иных редакций ландмарок. Майкл Ботелхо в своей статье «Масонские ландмарки»
    (http://www.srmason-sj.org/web/journal-files/Issues/Feb02/botelho.htm) – ряд общеизвестных фактов в которой,
    впрочем, изложен не совсем аккуратно – отмечает, что в 1863 году Джордж Оливер
    опубликовал&nbsp;<em>Freemason‘</em><em>s&nbsp;</em><em>Treasury,&nbsp;</em>где перечислил 40 ландмарок. В&nbsp;XXвеке
    по Ботелхо, ряд Великих Лож в США предпринимали попытки перечислить ландмарки: от Западной Вирджинии (7) и
    Нью-Джерси (10) до Невады (39) и Кентукки (54).</p>
<p>На самом деле ни одна из редакций ландмарок не имеет обязывающего значения. Фактически роль ландмарок в современном
    масонском мире выполняют принципы&nbsp;взаимного признания Великих Лож.</p>

<br>
<h2>Что такое Шотландский Устав?</h2>
<p>Древний и Принятый Шотландский Устав (Ancient and Accepted Scottish Rite) есть иерархическая система масонских
    организаций, которая является «надстройкой» над символическим масонством. Шотландский Устав не может рассматриваться
    как организация, которая руководит символическим масонством – он не признается официально масонством 1-3 степеней.
    Великие Ложи, управляющие 1-3 степенями, абсолютно независимы от организаций Шотландского Устава и не признают более
    высокой степени, чем степень мастера-каменщика (3 градус). Скорее, имеет место зависимость Шотландского Устава от
    Великих Лож. Быть членом организации Шотландского Устава можно только являясь мастером вольным каменщиком и
    оставаясь активным членом символической ложи. Регулярные масоны считают, что древнее цеховое масонство состоит лишь
    из трех символических градусов. Эта позиция была официально сформулирована материнской для всего масонства Великой
    Объединенной Ложей Англии в 1813 г. и с тех пор наличие трех градусов является одной из неизменных ландмарок.</p>
<p>Шотландский Устав характеризуется развитой обрядностью и богатой посвятительной практикой. Он включает в себя степени
    с 4 по 33, ритуалы, уроки и заповеди этих степеней развивают моральное учение символических градусов. В этом смысле
    Шотландский Устав продолжает традиции символического масонства. Несмотря на то, что структуры Шотландского Устава
    независимы от Великих Лож, эти структуры, также как и Великие Ложи, достаточно тесно интегрированы во всемирную
    братскую цепь на своем уровне.</p>
<p>Высшим руководящим органом структур Шотландского Устава на территории юрисдикции является Верховный Совет. Как
    правило, территория юрисдикции органов Шотландского Устава совпадает с территорией страны. Исключение составляют
    Соединенные Штаты Америки, где действуют два Верховных Совета: Верховный Совет Северной Юрисдикции и Верховный Совет
    Южной Юрисдикции. Верховный Совет Южной юрисдикции США является самым древним Верховным Советом в мире.</p>
<p>История происхождения Древнего и Принятого Шотландского Устава по Бр. Маккленахану такова:</p>
<p>«Приблизительно во времена нового пробуждения интереса к масонству в начале XVIII века, адепты Ордена чувствовали
    необходимость более глубокого проникновения в арканы франкмасонства и более глубокого знакомства с эзотерической
    доктриной. Самые развитые и блестящие умы Европы, решив заняться исследованием Каббалы и будучи очарованы
    неизмеримыми глубинами, открывшимися перед ними, решили учредить новую, более высокую ступень франкмасонства, только
    и лишь ради пропаганды и изучения древних мистерий, почти неизвестных им до тех пор, которая состояла бы из
    исторических, философских и рыцарских градусов. Имея в виду эту цель, они сперва стремились создать некие отдельные
    и независимые организации только для того, чтобы внутри них культивировались и раскрывались эти великие истины.
    Практически все эти проекты пережили своих инициаторов, однако «Устав Усовершенствования» (Emulation), эмбрион
    организационной структуры Древнего и Принятого Шотландского Устава – основанный на чистейших масонских принципах,
    содержавший в себе раскрытие разнообразных оккультных мистерий и состоявший из двадцати пяти градусов – постепенно
    начал развиваться.</p>
<p>Без всякого сомнения, деятельность кавалера Рамзая в 1740 г. ускорила усвоение, систематизацию и полное признание
    масонством тех градусов, которые оно в течение долгого времени отвергало и не признавало. Противники Древнего и
    Принятого Шотландского Устава, такие как Финдейл и прочие, утверждают, что Устав начал свое развитие около 1740 г.
    под руководством Майкла Эндрю Рамзая, более широко известного под титулом «кавалер Рамзай», родившегося в Шотландии,
    в Эйре, в 1686 г., и умершего в Сен-Жермен-де-Ле, что во Франции, в 1743 г.; что со времени изгнания Стюартов из
    Англии в 1688 г. между Римом, куда бежал в 1719 г. претендент на престол из династии Стюартов, и Шотландией
    существовали постоянные тайные связи; что по мере развития этих связей претендент все укреплялся в своих надеждах на
    благоприятный для него исход; что Рамзай пытался подорвать дух верноподданничества и верности Престолу в Великой
    Ложе Шотландии, основанной в 1736 г., а осознав безрезультатность своих попыток, предпринял попытку создать и как
    можно теснее сплотить свою собственную группу, состоящую из приверженцев изгнанной королевской династии, в высших
    масонских степенях посвящения, таким образом, потакая их честолюбивым устремлениям; что масонские ложи во Франции
    были образованы шотландскими заговорщиками вместе с членами и сторонниками ордена Иезуитов, которые пали столь
    низко, что решили разыграть свою карту в этом новом деле, сулившем разнообразные и весомые перспективы на
    политической сцене.</p>
<p>Некоторые исследователи утверждают, что вышеупомянутый Устав Усовершенствования был основан в 1753 г., в то время как
    другие настаивают на том, что только в 1758 г. несколько масонов, именовавших себя «Верховными Князьями и Великими
    Офицерами Великой и Верховной Ложи Св.: Иоанна Иерусалимского», основали в Париже организацию, которую назвали
    «Советом Императоров Востока и Запада». Этот Совет и был более широко известен как Устав Усовершенствования
    (Emulation), и по мнению Тори, Рагона, Левекью, Видала, Феранди, Клавеля и прочих, состоял из двадцати пяти
    градусов; в 1759 г. он учредил в Бордо Совет Князей Королевской Тайны и с тех пор продолжал развиваться.</p>
<p>К 1761 г. ложи, советы, капитулы и консистории Устава значительно увеличились в числе и распространились по всему
    европейскому континенту; 27-ого августа того же года Стефан Морен был назначен генералом-инспектором Нового Света
    указом Великой Консистории Князей Королевской Тайны, подписанным в Париже вице-генералом Ордена Шельоном де
    Жуанвиллем… 25-ого октября 1762 г. Великие Масонские Конституции были, наконец, ратифицированы в Берлине, в Пруссии,
    и предложены в качестве единственного и окончательного кодекса, регулирующего течение работ во всех ложах, советах,
    капитулах, коллегиях и консисториях Древнего и Принятого Шотландского Устава в обоих полушариях. В том же году они
    были пересланы генералу-инспектору Стефану Морену, который признал их… 1-ого мая 1786 г. Верховным Советом
    Высочайших Великих Генералов-Инспекторов 33-его и Последнего Градуса были выпущены собственные Конституции,
    впоследствии также признанные Берлином. Никаких иных верховных советов высочайших великих генералов-инспекторов до
    этого времени нигде в мире образовано не было; до этого времени обязанности и права, соответствующие этим и
    некоторым нижестоящим организациям, возлагались на вице-генералов-инспекторов. По новым Конституциям, все права
    верховной власти отдавались Верховным Советам, состоящим в каждой стране из девяти братьев, принимающим на себя
    также и все связанные с этими правами обязанности, и всю соответствующую им ответственность; исключение было сделано
    только для Соединенных Штатов Америки, в которых были образованы два Верховных Совета с разделением сфер
    деятельности по территориальному признаку. Верховный Совет 33-его Градуса для Англии, Уэльса и пр. был
    конституционно образован 26-ого октября 1845 г.»</p>
<p>Цитата приведена по книге Гумберта С. Бокса «Природа франкмасонства».</p>

