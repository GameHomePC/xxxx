<h2>Что такое символическое масонство?</h2>
<p>Символическое масонство, то же самое, что&nbsp;Blue&nbsp;Lodges,&nbsp;Craft. Древнейшая система масонства, состоящая,
    как правило, из трех основных степеней ученика, подмастерья и мастера. Посвященные этих степеней объединены в ложи.
    Высшее административное управление степенями и ложами осуществляют особые масонские административные образования –
    Великие Ложи. Символическое масонство – основа масонства, древнейшая его часть.</p>
<p>К моменту создания Великой Ложи&nbsp;в 1717 году масонство работало в двух степенях: ученика (entered&nbsp;apprentice)
    и подмастерья (fellow&nbsp;craft), исторически связанных с традициями оперативного масонства. В оперативном
    масонстве за пределами ложи, как отмечал Гарри Карр (Harry Carr’s The Freemason at Work. London: Burgess &amp; Sons,
    1976), за пределами ложи один был работником, а другой нанимателем. Однако внутри ложи, каждый был равен другому,
    оба считались полностью обученными масонами и проходили через одну и ту же церемонию.</p>
<p>В то время мастером назывался управляющий офицер ложи. Древнейшее упоминание о степени мастера содержит манускрипт
    1711 года, хранящийся в&nbsp;Trinity&nbsp;College&nbsp;Dublin&nbsp;MS.</p>
<p>1725 годом датированы первые протоколы о работе в степени мастера по данным ОВЛА. Первым широко известным
    свидетельством посвятительных работ в масонской степени мастера стала публикация памфлета Сэмюэла Причарда (Samuel&nbsp;Prichard.&nbsp;Masonry&nbsp;Dissected.&nbsp;London.
    1730), содержавшая катехизис ученика из 92 вопросов и ответов, подмастерья из 33, и мастера из 30 вопросов и
    ответов.</p>
<p>С 1730-х годов Великая Ложа Англии «Премьер» работает в трех символических степенях.&nbsp;В 1751 году в Англии
    возникает «Великая Ложа Вольных и Принятых каменщиков в соответствии со Старыми Установлениями», которую в масонской
    литературе называют «Древняя» Великая Ложа. Она практикует Степень Царственного Свода (Королевской Арки) как
    ключевую масонскую степень. В 1813 году «Древняя» и «Современная» Великие Ложи Англии объединяются и декларируют,
    что «истинное и древнее масонство состоит из трех степеней и не более, каковыми являются степени Ученика,
    Подмастерья и Мастера Масона, включающая Верховный Орден Святого Царственного Свода».</p>
<p>Правило работы в трех степенях под юрисдикцией Великой Ложи становится Базовым принципом признания ОВЛА и включается
    в список условий масонской регулярности. Так, пункт 5 Базовых принципов признания Объединенной Великой Ложи Англии
    от 4 декабря 1929 г. устанавливает: «Великая Ложа обладает суверенной юрисдикцией над всеми ложами под ее контролем,
    то есть это ответственная, независимая и самоуправляемая организация с единой и неоспоримой властью над цеховыми или
    символическими степенями (Ученик, Подмастерье, Мастер) в рамках своей юрисдикции».</p>
<p>Говоря иными словами, в масонском мире с этого момента символическое масонство включает в себя три степени
    повсеместно и в некоторых случаях может легитимно включать в себя степень Царственного Свода. Символическое
    масонство в России, управляемое&nbsp;Великой Ложей России, работает в трех степенях: ученик, подмастерье, мастер.
    Существует масса разнообразных дополняющих организаций, объединяющих мастеров масонов в различные уставы и степени,
    помимо членства мастеров масонов в символических ложах под юрисдикцией великих лож. Однако каждый масон должен
    всегда оставаться членом какой-либо символической ложи, и потеря им членства в символической ложе влечет его
    исключение из всех дополняющих организаций.</p>
<p>Кроме того, верно и то, что единственный путь в масонство лежит через символические ложи, в которых, и только в
    которых, кандидат может быть допущен в первую степень.</p>
<p>Символическое масонство практикует разные ритуалы. Однако, вот на что обращает внимание К.Уэйт, вычленяя суть
    масонских степеней цеха: «При ближайшем рассмотрении обнаруживается, что цеховое масонство включает в себя три
    элемента любопытным образом связанные с подрубрикой символической&nbsp;архитектуры… Во-первых, есть собственный труд
    кандидата, во время которого его учат строительству самого себя. Метод обучения по сути своей весьма практичен и,
    будучи широко известен и открыт, в общем не является содержанием занятий тайного ордена. Во-вторых, есть миф о
    здании, и способ его представления вынуждает кандидата принимать участие в драматическом ритуале, во время которого
    он обнаруживает живую связь с масонским Мастером-строителем. И в-третьих, есть масонский поиск, связанный с понятием
    тайного слова, что сообщается как существенная часть мастерской степени строительства. Возможно, это самый важный их
    трех элементов, однако поиск слова остается незавершенным в третьей степени».</p>
<p>Во многих юрисдикциях символическое масонство называется также Иоанновским масонством. Многие масонские авторы
    полагают, что учение символического масонства связано с покровительством Иоанна Крестителя и&nbsp;Иоанна
    Богослова.</p>

