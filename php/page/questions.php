<h2>Что такое Орден вольных каменщиков?</h2>
<p>Орден вольных каменщиков (масонство) – братство, чьими основными принципами являются братская любовь, помощь ближнему
    (милосердие) и приверженность истине.</p>
<p>Выражаясь кратко, цель масонства «сделать хорошего человека лучше». Люди подмечают, что зачастую человек является
    продуктом того, что он встретил в жизни, своего окружения. Орден предлагает человеку возможность быть в компании
    людей морали и долга, позволяя ему развивать собственный дух и помогать в этом другим людям. При этом одна из целей
    Ордена сделать такое развитие для всех людей приятным, именно поэтому он является братством, а не просто философским
    клубом.</p>
<p>Для того, чтобы устранить все, что может разъединить людей, дискуссии о политике и религии в ложах запрещены и само
    регулярное масонство никогда не занималось и не занимается ни политикой, ни каким-либо видом религиозной пропаганды.
    Масонство принимает любую веру и любые политические убеждения своих членов, не пытаясь переубедить их и желает,
    чтобы братья были искренне верующими. Но масонство никогда не призывает никого из своих членов исповедовать
    какую-либо особо указанную религию, следовать какому бы то ни было конкретному духовному пути, поддерживать
    какую-либо партию или какую-либо идеологическую доктрину.</p>
<p>Масонство активно использует в символике рабочие инструменты каменщиков, но это не означает, что масонство как-то
    связанно с каким-либо видом строительных работ.</p>
<p>Масонство исключительно законопослушная организация. Ничто ни в ритуалах, ни в регламентах масонов не противоречит
    действующему законодательству той страны, где масоны работают. Если власти где-либо запрещают деятельность масонских
    организаций, такая деятельность немедленно прекращается. Однако&nbsp; такая деятельность будет возобновлена при
    первой же законной благоприятной возможности.</p>
<p>Масонство отличает от других орденов внимание к морали, нравственному и духовному развитию, развитая ритуалистка,
    регламентированность деятельности,&nbsp;подчеркнутая законопослушность. Орден вольных каменщиков никогда не был
    организацией, служащей какой-либо церкви или вероисповеданию и пытающейся насадить ее владычество где-либо. Никогда
    Орден не был организацией, служащей какому-либо одному правительству, организацией, заставляющей своих членов
    действовать вопреки интересам своей родины. Масоны всегда были патриотичными людьми, верными своей стране.</p>
<p>В современной форме масонство регулярное символическое масонство оформилось в XVII веке, но уже с XIV века
    сохранились документальные достоверные свидетельства о существовании оперативного масонства, которое является
    предтечей Ордена в его современном виде. И многое в обрядности и символизме Ордена вольных каменщиков, в традициях
    его посвятительной практики восходит к гораздо более древним временам.</p>
<p>Масонство не является ни религией, ни контролирующей мир организацией.</p>
<p>Мировое масонство не имеет единого руководящего центра.</p>

