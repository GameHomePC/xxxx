<h2>Какая масонская должность самая высшая и самая почетная Великий командор Верховного Совета 33 градуса или Великий
    Мастер Великой Ложи?</h2>
<p>Вопрос поставлен не по-масонски. Масон не может ответить на вопрос в такой форме, потому что все братья независимо от
    степени своего посвящения и своих должностей, прежде всего, братья. Мы можем только упомянуть о том, что этот вопрос
    регулярно обсуждается в Сети профанами на различных западных масонских веб-форумах. Как правило, они приходят к
    заключению, что самая почетная масонская должность на территории юрисдикции – Великий Мастер Великой Ложи.</p>

<br>
<h2>Что такой «Йоркский Устав»?</h2>
<p>Йорский Устав есть также одна из надстроек над символическим масонством, как и Шотландский Устав. В США Йоркский
    устав включает в себя восемь дополнительных градусов сверх трех признанных ландмарками градусов символического
    масонства, а также почетное право быть посвященным в три рыцарских ордена (Орден Рыцарей храма, Орден Красного
    Креста и Орден Рыцарей Мальты). Называть рыцарские Ордена Йоркского Устава степенями (градусами) посвящения
    неправильно. Принятие в рыцарские ордена сопровождается принесением конфессиональной (христианской) клятвы, что
    нетипично для масонства. В других странах (кроме США) количество и структура степеней Йоркского Устава может
    меняться, но общим остается то, что сразу вслед за символическими орденами следует посвящение в степени Королевской
    Арки (как правило, их 4), а высшим посвящением Йоркского Устава является принятие в Орден рыцарей храма. В отличие
    от Йоркского Устава количество степеней Шотландского Устава не меняется. В настоящее время структур Йоркского Устава
    в России нет.</p>

<br>
<h2>Что такое «Восточная Звезда»?</h2>
<p>Орден Восточной Звезды – парамасонский орден, в основе идеологии которого лежит учение, базирующееся на Библии. Орден
    преследует, прежде всего, благотворительные цели. Основан Робертом Моррисом, адвокатом и из Бостона. Моррис
    первоначально намеревался организовать женское направление в масонстве, но не смог победить сильное противодействие.
    В 1849 году Моррисом был впервые опубликован ритуал Ордена Восточной Звезды. Впоследствии он работал вместе с
    Робертом Макоем, который написал и в 1867 г. опубликовал ритуал, базирующийся на Моррисовском ритуале. В том же году
    была организована первая ложа. Члены Ордена должны быть старше 18 лет и являться либо хорошо зарекомендовавшими себя
    Мастерами, либо близкими родственниками хорошо зарекомендовавших себя Мастеров. Каждое Отделение имеет 18 офицеров,
    некоторые из которых выбраны, а остальные назначены. Двое офицеров должны быть мужчинами, девять офицеров –
    женщинами. Вся символика и все учение Ордена основано на Ветхом и Новом Заветах. Но членство в Ордене требует веры
    только в Высшие Бытие, а не принадлежности какой-либо конфессии или только христианского вероисповедания.</p>

