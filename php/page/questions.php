    Американские Ложи регулярного Масонства также сразу признали Великий Восток Италии. Возродилась Национальная Великая
    Ложа Италия, котораяпо-прежнему оставалась нерегулярной Ложей, лишенной признания в масонском мире. После широкого
    признания Великого Востока большинство Лож, подчиняющихся Национальной Ложе, перешли под юрисдикцию Великого
