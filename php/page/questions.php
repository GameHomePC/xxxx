    братьев-рекомендаторов, то его вопрос обсуждается в руководстве Великой Ложи России.</p>
<p>После этого ложа рассматривает кандидатуру соискателя. При вынесении положительного решения кандидат допускается до
    опроса «под повязкой». Кандидат приглашается в ложу, где во время торжественных ритуальных работ он с завязанными
    глазами вводится в собрание и члены ложи задают надлежащие вопросы.</p>
<p>После члены ложи обсуждают вопрос еще раз и принимают окончательное решение. Кандидатура считается отклоненной, если
    хотя бы три члена ложи проголосовали против. Однако даже если один или два члена ложи резко высказались против
    кандидата, это может значительно отложить во времени посвящение.</p>
<p>В случае принятие положительного решения кандидату сообщается дата и время посвящения. Вся процедура может занять
    много месяцев.</p>

