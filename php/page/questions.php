<h2>Какие есть еще масонские организации?</h2>
<p>Кроме этого есть множество организаций. Приведем названия лишь некоторых.&nbsp;<strong>Акация:</strong>братство
    Мастеров, сыновей Мастеров или молодых людей, рекомендованных двумя Мастерами, один из которых является членом
    Акации.&nbsp;<strong>Орден Амарантов:</strong>&nbsp;Орден Масонов, их жен, матерей, дочерей, вдов и
    сестер.&nbsp;<strong>Филалеты:</strong>&nbsp;группа Масонов, заинтересованных в изучении философии и истории
    Масонства.&nbsp;<strong>Королевский Орден Шотландии:</strong>&nbsp;организация Масонов-христиан, которые являются
    масонами 32 степени ДПШУ или Рыцарями Храма в течение не менее, чем пяти лет.</p>

