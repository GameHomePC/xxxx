    Италии.</p>
<p>История итальянского масонства берет свое начало в 1750-х годах, когда были организованы несколько масонских лож,
    деятельность которых вскоре была запрещена. Возродилось масонство в Италии только в 1860-х годах. Несмотря на то,
    что обсуждение политических и религиозных вопросов в регулярных ложах запрещено, этот правило нарушалось в Великом
    Востоке Италии довольно часто. На этом фоне в 1908 году в итальянском масонстве произошел раскол, в результате
    которого множество наиболее активных сторонников политических дискуссий было вынуждено покинуть Великий Восток
    Италии. Ими была сформирована так называемая Национальная Великая Ложа, которая существует как нерегулярная Великая
    Ложа до сих пор.</p>
<p>В период с 1926 по 1945 год итальянское масонство было запрещено режимом Муссолини. После снятия запрета возродился
