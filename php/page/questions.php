    на роспуск P2 в марте 1975 года Джелли обвинил Великого Мастера Великого Востока Италии в финансовых махинациях. В
    период до 1980 года Джелли инициировал ряд скандалов, опубликовал серию внутренних масонских документов, однако
    начиная с 1974 года история P2, строго говоря, не есть история масонской ложи, а история самодеятельного клуба с
    масонским антуражем.</p>
<p>Однако несмотря на роспуск P2, сам Джелли оставался масоном вплоть до 1981 года, когда был «усыплен» за деятельность,
    противоречащую интересам ордена. В том же году полиция связала имя Джелли со многими криминальными действиями. Во
    время обыска его дома были найдены списки 950 членов к тому времени нелегальной ложи, многие из которых были
    известными людьми. Джелли был вынужден покинуть Италию.</p>
<p>Президент парламентской комиссии по расследованиям признал, что деятельность P2 не была деятельностью масонской
    организации и что именно масонство, в первую очередь, пострадало от деятельности P2. Тем, кто особо заинтересован в
    изучении истории P2, мы рекомендуем обратиться к следующим работе:</p>
<p>The Transactions of the Lodge of Research No. 218. «Italian Freemasonry and the ‘P2′ Incident», Kent Henderson.
    Victoria, Australia: 1987 pp. 25-33. [ISBN 0 7316 2645 1].</p>

<h5>Правда ли, что масоны – экуменисты?</h5>
<p>Экуменизм – движение за объединение всех церквей. Масонство признает в крупнейших мировых религиях единый духовный
    источник, общую глубинную основу. Но эта основа может иметь различные формы проявления и масонство абсолютно терпимо
    относится к многообразию форм реализации религиозного чувства. Регулярное масонство принципиально не вмешивается во
    внутренние дела церквей и по этой причине не может быть названо экуменистской организацией. Представления о
    практической возможности создания единой религии по меньшей мере наивны. На самом деле характеристики масонства как
    экуменистской организации – излюбленный и надуманный прием тех, кто хочет искусственно столкнуть масонство и с
    какой-либо из крупных мировых религий.</p>

