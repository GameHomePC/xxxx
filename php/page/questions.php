<h2>Какова история масонства в России?</h2>
<p>Считается, что история масонства в России началась с Петра Первого, который принял посвящение в 1699 году от
    Кристофера Рена.. Но эта версия не имеет никакого документально подтверждения. После того, как было «прорублено окно
    в Европу», то есть с конца XVII века, в Московское Царство хлынули представители западноевропейской культуры. Среди
    них были техники, профессора, философы, строители, проповедники. В 1689 году в Москву приехал последователь Якова
    Беме, немецкий мистик Квирин Кульман. Он верил в миссию «северного народа» и написал воззвание к московским царям.
    Для передачи этого воззвания он и приехал в Москву. Свой проповедью он внес большое возбуждение в жизнь Немецкой
    Слободы и 4 октября 1689 года был сожжен. С ним в Россию проникла и начала распространяться старинная герметическая
    премудрость. Она и проложила дорогу масонству. Согласно преданию, первая ложа возникла еще в царствование Алексея
    Михайловича и Досточтимым Мастером ее был Брюс. Как можно заметить, это частично подтверждает приведенное выше
    утверждение о том, что масонство было «завезено» в Россию Петром Великим. Есть мнение, что Петр был принят в
    шотландскую степень Святого Андрея, и это, например, могло повлиять на его выбор флага Военно-Морского Флота России.
    Российские масоны вплоть до времен Екатерины II были в основном иностранцы по происхождению. Начиная с 1760 годов
    уже фактически каждая дворянская семья имела хотя бы одного человека в масонах. Достаточно перечислить наиболее
    известных масонов Елизаветинских и начала Екатерининских времен. Это кн. И. В. Несвицкий, гр. Р. Л. Воронцов, А. Л.
    Щербачев, С. В. Перфильев, гр. С. Р. Воронцов, бар. К. Унгерн-Штренберг, А. Войеков, кн. Андрей Вяземский, гр. В.
    Фермор, кн. А. Одоевский, князья Гагарины, Долгорукие, Куракины, кн. Н. В. Репнин, графы А. С. Строганов, А. И.
    Мусин-Пушкин, Шуваловы, кн. Трубецкие, кн. Репнины, кн. Черкасский, Лодыженские, Лопухины и мн. др. В масонстве
    произошел рост этих людей от рационалистических идей вольтерианства до духовно-мистических базирующихся на Каббале,
    учении православных Святых Отцов . Немного подробнее идеология, философия, благотворительность кратко.. далее вплоть
    до настоящего времени. Лучше понять историю российского масонства вы можете ознакомившись с кратким списком
    российских масонов</p>

